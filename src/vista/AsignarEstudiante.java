    private void btnAsignarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAsignarMouseClicked

        Curso curso = (Curso) listaCursos.getSelectedItem();
        Estudiante estudiante = (Estudiante) listaEstudiantes.getSelectedItem();
//se valida que haya seleccionado algo
        if (curso == null || estudiante == null) {
            JOptionPane.showMessageDialog(rootPane, "Debes Seleccionar un  curso y un estudiante");
        } else {
            try {
//si pasa las validaciones se crea la asignaci√≥n
                controladorEstudianteCurso.crearEstudianteCurso(estudiante, curso);

            } catch (IOException e) {
                JOptionPane.showMessageDialog(rootPane, "Error al asignar al estudiante");
            }
        }


    }//GEN-LAST:event_btnAsignarMouseClicked


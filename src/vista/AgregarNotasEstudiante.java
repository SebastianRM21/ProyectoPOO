    private void btnAgregarNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarNotaActionPerformed

        String notaUno = inputNota1.getText();
        String notaDos = inputNota2.getText();
        String notaTres = inputNota3.getText();
//validamos que las notas sean numericas
        if (notaControl.VerificarDouble(notaUno, notaDos, notaTres)) {
//validamos que las notas esten en el rango
            if (!notaControl.validacionNotas(notaUno, notaDos, notaTres)) {
                JOptionPane.showMessageDialog(rootPane, "Algunos campos estan erroneos, la nota debe ir en un rango de 0 a 5");
            } else {

                Estudiante estudianteSeleccionado = (Estudiante) listaEstudiante.getSelectedItem();
                Curso cursoSeleccionado = (Curso) listaCurso.getSelectedItem();
//validamos que haya seleccionado un curso y un estudiante 
                if (estudianteSeleccionado == null || cursoSeleccionado == null) {
                    JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un curso y un estudiante");
                } else {

//sicumple todas las validaciones se crea la asignaci√≥n
                    try {
                        notaControl.actualizarNota(cursoSeleccionado.getCodigo(), estudianteSeleccionado.getIdentificacion(), notaUno, notaDos, notaTres);
                        lblNotaPromedio.setText("Promedio actual: " + notaControl.CalcularNota(new ArrayList<>(Arrays.asList(Double.parseDouble(notaUno), Double.parseDouble(notaDos), Double.parseDouble(notaTres)))));
                        JOptionPane.showMessageDialog(rootPane, "Nota Actualizada");
                    } catch (IOException e) {
                        JOptionPane.showMessageDialog(rootPane, "Error al actualizar la nota en el archivo");
                    }

                }

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Por favor, llene los campos correctamente");
        }
    }//GEN-LAST:event_btnAgregarNotaActionPerformed


    private void listaCursoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_listaCursoItemStateChanged
//actualiza la lista de estudiantes dependiendo del curso seleccionado
        notaControl.camposVacios(inputNota1, inputNota2, inputNota3, lblNotaPromedio);
        try {
            Curso cursoSeleccionado = (Curso) listaCurso.getSelectedItem();
            controladorEstudiante.listaEstudianteCurso(listaEstudiante, cursoSeleccionado.getCodigo());

        } catch (IOException e) {
            System.out.println("Error en listar estudiantes");
        }

    }//GEN-LAST:event_listaCursoItemStateChanged

    private void listaEstudianteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_listaEstudianteItemStateChanged
//trae las notas del estudiante seleccionado en los campos
        lblNotaPromedio.setText("");
        Estudiante estudianteSeleccionado = (Estudiante) listaEstudiante.getSelectedItem();
        Curso cursoSeleccionado = (Curso) listaCurso.getSelectedItem();
        try {
            inputNota1.setText("" + notaControl.notaEstudiante(cursoSeleccionado.getCodigo(), estudianteSeleccionado.getIdentificacion()).get(0));
            inputNota2.setText("" + notaControl.notaEstudiante(cursoSeleccionado.getCodigo(), estudianteSeleccionado.getIdentificacion()).get(1));
            inputNota3.setText("" + notaControl.notaEstudiante(cursoSeleccionado.getCodigo(), estudianteSeleccionado.getIdentificacion()).get(2));
        } catch (IOException e) {
            JOptionPane.showMessageDialog(rootPane, "Error al obtener las notas del estudiante");
        }


    }//GEN-LAST:event_listaEstudianteItemStateChanged

